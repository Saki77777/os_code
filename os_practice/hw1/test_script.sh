#!/bin/bash

# 打印脚本开始运行的时间
echo "测试脚本开始运行，当前时间：$(date)"

# 检查系统基本信息
echo "-----------------------------"
echo "系统基本信息："
echo "-----------------------------"
echo "主机名：$(hostname)"
echo "操作系统信息：$(uname -a)"
echo "当前用户：$(whoami)"
echo "当前路径：$(pwd)"

# 检查磁盘使用情况
echo "-----------------------------"
echo "磁盘使用情况："
echo "-----------------------------"
df -h

# 检查内存使用情况
echo "-----------------------------"
echo "内存使用情况："
echo "-----------------------------"
free -h

# 检查网络连接状态
echo "-----------------------------"
echo "网络连接状态："
echo "-----------------------------"
ping -c 4 google.com

# 检查常用服务状态（根据需要修改服务名称）
echo "-----------------------------"
echo "常用服务状态："
echo "-----------------------------"
systemctl is-active --quiet sshd && echo "SSH服务：运行中" || echo "SSH服务：未运行"
systemctl is-active --quiet apache2 && echo "Apache服务：运行中" || echo "Apache服务：未运行"

echo "-----------------------------"
echo "测试脚本运行结束，当前时间：$(date)"
